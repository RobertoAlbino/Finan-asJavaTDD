    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        // TODO add your handling code here:
        //validar campos
        
        try{
            if (textId.getText().isEmpty()){//registro novo
                dao.addEquipamento(populateObject());
            }else{//registro alterado
                dao.updateEquipamento(populateObject());
            }
            //refresh de tela
            iniciaComponentes();
            carregaEquipamento();        
        }catch(ParseException ex){
            System.out.println("ERRO DATA"+ex.getMessage());
        }catch(SQLException ex){
            System.out.println("ERRO:"+ex.getMessage());
        }
    }//GEN-LAST:event_buttonSalvarActionPerformed

    private void tableEquipamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEquipamentoMouseClicked
        // TODO add your handling code here:
        try{
            if (evt.getClickCount() == 2){
                int codigo=
                Integer.parseInt(tableEquipamento.getValueAt(
                        tableEquipamento.getSelectedRow(), 0)+"");        
                populateComponentes(dao.getEquipamento(codigo));
            }
        }catch(SQLException ex){
            System.out.println("ERRO:"+ex.getMessage());
        }
    }//GEN-LAST:event_tableEquipamentoMouseClicked

    private void buttonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRemoverActionPerformed
        // TODO add your handling code here:
        if (textId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione um Registro com Duplo Clique");
            return;
        }
        if (JOptionPane.showConfirmDialog(null, "Confirma?")!=0){
            return;
        }
        try{
            dao.deleteEquipamento(populateObject());
            //refresh
            iniciaComponentes();
            carregaEquipamento();
        }catch(ParseException ex){
            System.out.println("ERRO DATA:"+ex.getMessage());
        }catch(SQLException ex){
            System.out.println("ERRO:"+ex.getMessage());
        }
    }//GEN-LAST:event_buttonRemoverActionPerformed

