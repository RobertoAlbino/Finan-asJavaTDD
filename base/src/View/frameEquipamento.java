    private void carregaTipo(){
        DefaultComboBoxModel cbm = 
        new DefaultComboBoxModel(tipoEquipamento.values());
        comboTipo.setModel(cbm);                
    }
    private void carregaCliente() throws SQLException{
        DefaultComboBoxModel cbm = 
        new DefaultComboBoxModel(new Vector(new daoCliente().getClienteList()));
        comboCliente.setModel(cbm);
    }
    private void carregaMarca() throws SQLException{
        DefaultComboBoxModel cbm = 
        new DefaultComboBoxModel(new Vector(new daoMarca().getMarcaLista()));
        comboMarca.setModel(cbm);
    }
    private void carregaEquipamento() throws SQLException{
        List<Equipamento> lista = dao.getEquipamentoList();
        tableEquipamento.setModel(new MyTableModel(Equipamento.class, lista, tableEquipamento));
    }
    private void carregaEquipamento(String filtro) throws SQLException{
        List<Equipamento> lista = dao.getEquipamentoList(filtro);
        tableEquipamento.setModel(new MyTableModel(Equipamento.class, lista, tableEquipamento));
    }
    //limpar componentes de tela
    private void iniciaComponentes(){
        textId.setText("");
        comboTipo.setSelectedIndex(0);
        comboMarca.setSelectedIndex(0);
        comboCliente.setSelectedIndex(0);
        textDescricao.setText("");
        textAquisicao.setText("");
        textPesquisa.setText("");
        buttonNovo.requestFocus();
    }
    //montar objeto a partir da tela
    private Equipamento populateObject() throws ParseException{
        return new Equipamento(
                textId.getText().isEmpty()?0:Integer.parseInt(textId.getText()), 
                (tipoEquipamento)comboTipo.getSelectedItem(), 
                textDescricao.getText(), 
                formato.parse(textAquisicao.getText()), 
                (Cliente)comboCliente.getSelectedItem(), 
                (Marca)comboMarca.getSelectedItem());
    }
    //montar tela a partir de objeto
    private void populateComponentes(Equipamento equipamento){
        textId.setText(equipamento.getId()+"");
        comboTipo.setSelectedItem(equipamento.getTipo());
        textDescricao.setText(equipamento.getDescricao());
        textAquisicao.setText(formato.format(equipamento.getAquisicao()));
        comboCliente.setSelectedItem(equipamento.getCliente());
        comboMarca.setSelectedItem(equipamento.getMarca());
    }
