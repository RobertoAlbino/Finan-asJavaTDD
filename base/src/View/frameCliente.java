    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        // TODO add your handling code here:
        if (textnome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nome Obrigatório");
            textnome.requestFocus();
            return;
        }
        if (textEndereco.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Endereço Obrigatório");
            textEndereco.requestFocus();
            return;
        }
        if (textCPF.getText().equals("   .   .   -  ")){
            JOptionPane.showMessageDialog(null, "CPF Obrigatório");
            textCPF.requestFocus();
            return;
        }
        if (textNascimento.getText().equals("  /  /    ")){
            JOptionPane.showMessageDialog(null, "Nascimento Obrigatório");
            textNascimento.requestFocus();
            return;
        }        
        try{
            if (textId.getText().isEmpty()){//novo
                dao.addCliente(populateObject());
            }else{//alterar
                dao.updateCliente(populateObject());
            }
            iniciaComponente();
            carregaClientes();
        }catch(SQLException e){
            System.out.println("ERRO:"+e.getMessage());
        }catch(ParseException ex){
            System.out.println("ERRO DATA:"+ex.getMessage());
        }
        
    }//GEN-LAST:event_buttonSalvarActionPerformed

    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed
        // TODO add your handling code here:
        if (textId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione um Registro com Duplo Clique");
            buttonExcluir.requestFocus();
            return;
        }
        //
        if (JOptionPane.showConfirmDialog(null, "Confirma?")!=0){
            return;
        }
        try{
            dao.deleteCliente(populateObject());
            iniciaComponente();
            carregaClientes();
        }catch(SQLException ex){
            System.out.println("ERRO:"+ex.getMessage());
        }catch(ParseException ex){
            System.out.println("ERRO DATA:"+ex.getMessage());
        }
    }//GEN-LAST:event_buttonExcluirActionPerformed

