    //cria um objeto a partir dos componentes
    private Cliente populateObject() throws ParseException{
        Cliente cliente = new Cliente();
        cliente.setId(textId.getText().isEmpty()?0:
                Integer.parseInt(textId.getText()));
        cliente.setNome(textnome.getText());
        cliente.setEndereco(textEndereco.getText());
        cliente.setCpf(textCPF.getText());
        cliente.setNascimento(formato.parse(textNascimento.getText()));
        cliente.setCidade((Cidade)comboCidade.getSelectedItem());
        return cliente;
    }
    //preenche os componentes com um objeto
    private void populateComponentes(Cliente cliente){
        textId.setText(cliente.getId()+"");
        textnome.setText(cliente.getNome());
        textEndereco.setText(cliente.getEndereco());
        textCPF.setText(cliente.getCpf());
        textNascimento.setText(formato.format(cliente.getNascimento()));
        comboCidade.setSelectedItem(cliente.getCidade());
    }
    //montar a Table
    private void carregaClientes() throws SQLException{
        LinkedList<Cliente> lista = (LinkedList)dao.getClienteList();
        tableCliente.setModel(new MyTableModel(Cliente.class, 
                lista, 
                tableCliente));
    }
    private void carregaClientes(String filtro) throws SQLException{
        LinkedList<Cliente> lista = (LinkedList)dao.getClienteList(filtro);
        tableCliente.setModel(new MyTableModel(Cliente.class, 
                lista, 
                tableCliente));        
    }
    //carrega lista de cidades
    private void carregaCidades() throws SQLException{
        LinkedList<Cidade> lista = 
        (LinkedList)new daoCidade().getCidadeList();
        DefaultComboBoxModel cbm =
                new DefaultComboBoxModel(new Vector(lista));
        comboCidade.setModel(cbm);
    }
    
