    private void buttonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonGravarActionPerformed
        // TODO add your handling code here:
        //validar campos
        if (textNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nome ObrigatÃ³rio");
            textNome.requestFocus();
            return;
        }
        if (textTelefone.getText().equals("(  )     -    ")){
            JOptionPane.showMessageDialog(null, "Telefone ObrigatÃ³rio");
            textTelefone.requestFocus();
            return;
        }
        try{
            if (textId.getText().isEmpty()){//registro novo
                dao.addTecnico(populateObject());
            }else{//alteraÃ§Ã£o de registro
                dao.updateTecnico(populateObject());
            }
        }catch(SQLException ex){
            System.out.println("ERRO:"+ex.getMessage());
        }
        iniciaComponentes();
        carregaTecnicoTable();
    }//GEN-LAST:event_buttonGravarActionPerformed

    private void tableTecnicoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTecnicoMouseClicked
        // TODO add your handling code here:
        try{           
            if (evt.getClickCount()==2){
                int codigo=Integer.parseInt(
                        tableTecnico.getValueAt(
                        tableTecnico.getSelectedRow(), 0)+"");
                populateComponent(dao.getTecnico(codigo));
            }
        }catch(SQLException ex){
            System.out.println("ERRO:"+ex.getMessage());
        }
    }//GEN-LAST:event_tableTecnicoMouseClicked

    private void buttonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRemoverActionPerformed
        // TODO add your handling code here:
        if (textId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione Registro com Duplo Clique");
            buttonNovo.requestFocus();
            return;
        }
        if (JOptionPane.showConfirmDialog(null, "Confirma?")!=0){
            return;
        }
        try{
            dao.deleteTecnico(populateObject());
        }catch(SQLException ex){
            System.out.println("ERRO:"+ex.getMessage());
        }
        //refresh em tela
        carregaTecnicoTable();
        iniciaComponentes();
    }//GEN-LAST:event_buttonRemoverActionPerformed

